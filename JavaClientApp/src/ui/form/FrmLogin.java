    private boolean validate(String username, String password) {
        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite username", "Login", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite password", "Login", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (username.length() < 9) {
            JOptionPane.showMessageDialog(this, "Korisnicko ime je prekratko", "Login", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (!password.matches(".*[0-9]+.*")) {
            JOptionPane.showMessageDialog(this, "Lozinka mora da sadrzi i slova i brojeve", "Login", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
