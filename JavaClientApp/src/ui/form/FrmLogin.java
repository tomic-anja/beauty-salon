    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());

        if (validate(username, password)) {
            try {
                Employee employee = new Employee(username, password);
                employee = Controller.getInstance().login(employee);
                JOptionPane.showMessageDialog(this, employee.getName() + " logged in!", "Login", JOptionPane.INFORMATION_MESSAGE);
                new FrmMain(employee).setVisible(true);
                dispose();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Login unsucessful! ", "Login", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

