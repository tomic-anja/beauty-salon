    public void fillTable() {
        try {
            treatments = Controller.getInstance().getAllTreatment();
            try {
                TableModelTreatment tm = new TableModelTreatment(treatments);
                tblTreatments.setModel(tm);
            } catch (Exception ex) {
                System.out.println("Tabela nije uspesno popunjena");
                ex.printStackTrace();
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmTreatment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void fillTable(List<Treatment> treatments) {
        try {
            TableModelTreatment tm = new TableModelTreatment(treatments);
            tblTreatments.setModel(tm);
        } catch (Exception ex) {
            System.out.println("Tabela nije uspesno popunjena");
            ex.printStackTrace();
        }
    }

    public Treatment getSelectedTreatment() {
        return selectedTreatment;
    }

    private void searchByTreatmentType(String attribute) {
        resultTreatment.clear();
        String attribute1 = attribute.toUpperCase();
        for (Treatment treatment : treatments) {
            if (treatment.getTreatmentType().toString().equals(attribute1)) {
                resultTreatment.add(treatment);
            }
        }

        if (resultTreatment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nepostoje Tretmani za izabranu vrednost", "Greska", JOptionPane.ERROR_MESSAGE);
        }

        fillTable(resultTreatment);
    }

    private void searchByPrice(String attribute) {
        double price = Double.parseDouble(attribute);

        resultTreatment.clear();
        for (Treatment treatment : treatments) {
            if (treatment.getPrice() == price) {
                resultTreatment.add(treatment);
            }
        }

        if (resultTreatment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nepostoje Tretmanu za izabranu vrednost", "Greska", JOptionPane.ERROR_MESSAGE);
        }

        fillTable(resultTreatment);
    }

