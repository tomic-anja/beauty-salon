    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int yesNo = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da izbrisete klijenta?", "Potvrda", JOptionPane.YES_NO_OPTION);

        if (yesNo == JOptionPane.YES_OPTION) {
            try {
                treatments = Controller.getInstance().getAllTreatment();
                int row = tblTreatments.getSelectedRow();
                if (row != -1) {
                    selectedTreatment = treatments.get(row);

                    try {
                        Controller.getInstance().deleteTreatment(selectedTreatment);
                        JOptionPane.showMessageDialog(this, "Treatment deleted!\nGenerated ID: [" + selectedTreatment.getTreatmentID() + "]", "Delete Treatment", JOptionPane.INFORMATION_MESSAGE);

                        fillTable();
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(this, "Treatment not deleted!\n" + ex.getMessage(), "Delete Treatment", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Prvo izaberite red tabele", "Greska", JOptionPane.ERROR_MESSAGE);

                }
            } catch (Exception ex) {
                Logger.getLogger(FrmTreatment.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

